// 与えられた大学x年度x科目 から該当する大問メタデータ配列を取得
function getPastExamProblems(univ, year, subject) {
  const u = PAST_EXAMS[univ];
  if (!u || !u.exams[year]) return [];
  const yearData = u.exams[year];
  const subjData = yearData[subject] || [];
  return subjData;
}

// 過去問メタデータから、AIに渡すプロンプト片を組み立てる
function buildPastExamPromptFragment(univ, year, subject, 大問メタ配列) {
  if (!大問メタ配列 || 大問メタ配列.length === 0) {
    return `## 過去問類題モード
- 大学: ${univ}
- 年度: ${year}年度
- 科目: ${subject}
- （この年度・科目の詳細メタは未登録。${univ}の${subject}の一般的な出題形式で作問してください）`;
  }
  const detail = 大問メタ配列.map(p => {
    const parts = [`大問${p.大問}: ${p.テーマ}`];
    if (p.配点) parts.push(`配点${p.配点}点`);
    if (p.時間目安) parts.push(`時間目安${p.時間目安}`);
    if (p.学部) parts.push(`(${p.学部}学部)`);
    return `  - ${parts.join('・')}`;
  }).join('\n');
  return `## 過去問類題モード
- 大学: ${univ}
- 年度: ${year}年度
- 科目: ${subject}

この年度・科目の大問構成:
${detail}

**作問指示**:
1. 上記の大問構成・テーマ・配点配分を厳密に踏襲した**類題**を作成
2. 出題形式（選択肢数・字数制限・記述 or マーク）を合わせる
3. ${univ}の採点基準・難易度感を再現（この大学の実際の合格最低点を意識）
4. 原文は使わず、テーマ・難易度・形式が同等のオリジナル問題を出題
5. **【重要】各問題のJSONオブジェクトには、以下3フィールドを必ず追加**:
   - "source": "${univ} ${year}年度"
   - "大問": "対応する大問番号（例: 1, 2(A), 3 など文字列）"
   - "テーマ": "その大問のテーマ（簡潔に）"
   これにより、出力された問題の横に大学・年度・大問が自動でバッジ表示される`;
}

